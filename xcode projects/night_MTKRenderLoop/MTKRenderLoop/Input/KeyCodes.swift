public enum KeyCodes: UInt16 {
    //Special Chars
    case space             = 0x31
    case returnKey         = 0x24
    case enterKey          = 0x4C
    case escape            = 0x35
    case shift             = 0x38
    case command           = 0x37
    
    //DPad Keys
    case leftArrow         = 0x7B
    case rightArrow        = 0x7C
    case downArrow         = 0x7D
    case upArrow           = 0x7E
    
    //Alphabet
    case a                 = 0x00
    case b                 = 0x0B
    case c                 = 0x08
    case d                 = 0x02
    case e                 = 0x0E
    case f                 = 0x03
    case g                 = 0x05
    case h                 = 0x04
    case i                 = 0x22
    case j                 = 0x26
    case k                 = 0x28
    case l                 = 0x25
    case m                 = 0x2E
    case n                 = 0x2D
    case o                 = 0x1F
    case p                 = 0x23
    case q                 = 0x0C
    case r                 = 0x0F
    case s                 = 0x01
    case t                 = 0x11
    case u                 = 0x20
    case v                 = 0x09
    case w                 = 0x0D
    case x                 = 0x07
    case y                 = 0x10
    case z                 = 0x06
    
    //Top Numbers
    case zero              = 0x1D
    case one               = 0x12
    case two               = 0x13
    case three             = 0x14
    case four              = 0x15
    case five              = 0x17
    case six               = 0x16
    case seven             = 0x1A
    case eight             = 0x1C
    case nine              = 0x19
    
    //Keypad Numbers
    case keypad0           = 0x52
    case keypad1           = 0x53
    case keypad2           = 0x54
    case keypad3           = 0x55
    case keypad4           = 0x56
    case keypad5           = 0x57
    case keypad6           = 0x58
    case keypad7           = 0x59
    case keypad8           = 0x5B
    case keypad9           = 0x5C
}
